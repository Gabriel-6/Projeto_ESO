export const genarateEmail = () => {
    const tamanho = 15
    const caracteres = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'
    var palavra = ''
    for (let i = 0; i < tamanho; i++) {
        const letra = Math.floor(Math.random() * caracteres.length)
        palavra += caracteres.charAt(letra)
    }
    return palavra + '@gmail.com'
}